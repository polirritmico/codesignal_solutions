import unittest

from cellsJoining import solution


# @unittest.skip
class TestBase(unittest.TestCase):
    # @unittest.skip
    def test_case1(self):
        table = [
            "+----+--+-----+----+",
            "|abcd|56|!@#$%|qwer|",
            "|1234|78|^&=()|tyui|",
            "+----+--+-----+----+",
            "|zxcv|90|77777|stop|",
            "+----+--+-----+----+",
            "|asdf|~~|ghjkl|100$|",
            "+----+--+-----+----+",
        ]
        coords = [[2, 0], [1, 1]]
        expected = [
            "+----+--+-----+----+",
            "|abcd|56|!@#$%|qwer|",
            "|1234|78|^&=()|tyui|",
            "+----+--+-----+----+",
            "|zxcv 90|77777|stop|",
            "|       +-----+----+",
            "|asdf ~~|ghjkl|100$|",
            "+-------+-----+----+",
        ]
        output = solution(table, coords)
        self.assertEqual(output, expected)

